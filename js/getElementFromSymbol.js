function getElementFromSymbol(symbol)
{
  if(symbol === "h")
  {
    return "hydrogen";
  }
  if(symbol === "he")
  {
    return "helium";
  }
  if(symbol === "li")
  {
    return "lithium";
  }
  if(symbol === "be")
  {
    return "beryllium";
  }
  if(symbol === "b")
  {
    return "boron";
  }
  if(symbol === "c")
  {
    return "carbon";
  }
  if(symbol === "n")
  {
    return "nitrogen";
  }
  if(symbol === "o")
  {
    return "oxygen";
  }
  if(symbol === "f")
  {
    return "fluorine";
  }
  if(symbol === "ne")
  {
    return "neon";
  }
}
